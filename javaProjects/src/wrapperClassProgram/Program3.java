package wrapperClassProgram;

public class Program3 {
	public static void main(String[] args) {
		System.out.println(Integer.MAX_VALUE); // It will give maximum value a datatype can hold
		System.out.println(Integer.MIN_VALUE); // It will give minimum value a datatype can hold
		System.out.println(Integer.TYPE); // it will give datatype of object, in this case it is int
		System.out.println(Integer.SIZE); // it will give the size of datatype
		System.out.println(Integer.BYTES); // it will give the byte size of the datatype
		System.out.println("===================");
		
		System.out.println(Long.MAX_VALUE);
		System.out.println(Long.MIN_VALUE);
		System.out.println(Long.SIZE);
		System.out.println(Long.BYTES);
	}
}
